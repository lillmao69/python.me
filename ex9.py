# Создание переменной для имени
name = "Артем"

# Преобразование имени в верхний регистр
upper_case_name = name.upper()

# Преобразование имени в нижний регистр
lower_case_name = name.lower()

# Вывод обоих результатов
print("Имя в верхнем регистре:", upper_case_name)
print("Имя в нижнем регистре:", lower_case_name)
