# Запрос температуры у пользователя в градусах Цельсия
celsius = float(input("Введите температуру в градусах Цельсия: "))

# Вывод введенной температуры в градусах Цельсия
print("Температура в градусах Цельсия:", celsius)

# Преобразование температуры из Цельсия в Фаренгейты
fahrenheit = (celsius * 9/5) + 32

# Вывод температуры в градусах Фаренгейта
print("Температура в градусах Фаренгейта:", fahrenheit)
